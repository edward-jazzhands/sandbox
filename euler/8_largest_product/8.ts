// join into one large string
const num: string = nums.join('')
// get length of num
const len = num.length;
const scan_size = 13;
let largest_prod = 0;
for (let x = 0; x <= (len-scan_size); x++) {
    const digits_at_x = num.slice(x, x+scan_size);
    // split string
    const dig_arr_str = digits_at_x.split('');
    // convert all elements of array into numbers
    const dig_arr_nums = dig_arr_str.map(Number); 
    // get the product using reduce
    const product = dig_arr_nums.reduce((acc: number, n: number): number => {
            return acc * n
        }, 1
    )
    // compare
    if (product > largest_prod) {
        largest_prod = product;
    }
}
